#include "../includes/libft.h"

char	*ft_linetrim(char *s, char c)
{
	int			i;
	char		*s1;

	i = 0;
	while (s[i] != c)
		i++;
	s1 = ft_strsub(s, 0, i);
	free(s);
	s = s1;
	return (s);
}
